import 'dart:convert';

import 'package:flutter/foundation.dart';
import 'package:samachar_hub/feature_horoscope/domain/entities/horoscope_entity.dart';
import 'package:samachar_hub/feature_horoscope/domain/entities/horoscope_type.dart';

class HoroscopeModel extends HoroscopeEntity {
  HoroscopeModel({
    @required String id,
    @required String title,
    @required String author,
    @required DateTime publishedAt,
    @required HoroscopeType type,
    @required String aries,
    @required String taurus,
    @required String gemini,
    @required String cancer,
    @required String leo,
    @required String virgo,
    @required String libra,
    @required String scorpio,
    @required String sagittarius,
    @required String capricorn,
    @required String aquarius,
    @required String pisces,
    @required DateTime createdAt,
    @required DateTime updatedAt,
    @required bool isLiked,
    @required bool isCommented,
    @required bool isShared,
    @required bool isViewed,
    @required int commentCount,
    @required int likeCount,
    @required int shareCount,
    @required int viewCount,
  }) : super(
          id: id,
          title: title,
          author: author,
          publishedAt: publishedAt,
          type: type,
          aries: aries,
          taurus: taurus,
          gemini: gemini,
          cancer: cancer,
          leo: leo,
          virgo: virgo,
          libra: libra,
          scorpio: scorpio,
          sagittarius: sagittarius,
          capricorn: capricorn,
          aquarius: aquarius,
          pisces: pisces,
          createdAt: createdAt,
          updatedAt: updatedAt,
          isLiked: isLiked,
          isCommented: isCommented,
          isShared: isShared,
          isViewed: isViewed,
          commentCount: commentCount,
          likeCount: likeCount,
          shareCount: shareCount,
          viewCount: viewCount,
        );
  factory HoroscopeModel.fromJson(String str) =>
      HoroscopeModel.fromMap(json.decode(str));

  String toJson() => json.encode(toMap());

  factory HoroscopeModel.fromMap(Map<String, dynamic> json) => HoroscopeModel(
        id: json["id"].toString(),
        title: json["title"],
        author: json["author"],
        publishedAt: DateTime.parse(json["publishedAt"]),
        type: (json["type"] as String).toHoroscopeType,
        aries: json["aries"],
        taurus: json["taurus"],
        gemini: json["gemini"],
        cancer: json["cancer"],
        leo: json["leo"],
        virgo: json["virgo"],
        libra: json["libra"],
        scorpio: json["scorpio"],
        sagittarius: json["sagittarius"],
        capricorn: json["capricorn"],
        aquarius: json["aquarius"],
        pisces: json["pisces"],
        createdAt: DateTime.parse(json["created_at"]),
        updatedAt: DateTime.parse(json["updated_at"]),
        isLiked: json["is_liked"],
        isCommented: json["is_commented"],
        isShared: json["is_shared"],
        isViewed: json["is_viewed"],
        commentCount: json["comment_count"],
        likeCount: json["like_count"],
        shareCount: json["share_count"],
        viewCount: json["view_count"],
      );
}
